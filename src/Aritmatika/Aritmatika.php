<?php

namespace Aritmatika;

class Aritmatika
{
	public function penjumlahan($a, $b)
	{
		return $a + $b;
	}

	public function pengurangan($a, $b)
	{
		return $a - $b;
	}

	public function perkalian($a, $b)
	{
		return $a * $b;
	}

	public function pembagian($a, $b) 
	{
		return $a / $b;
	}

	public function hasilBagi($a, $b)
	{
		return $a % $b;
	}
}